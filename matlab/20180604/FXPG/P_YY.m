%%调用故障概率模型
%%%%1、诱因性故障概率
function y=P_YY(x)         %x为变压器状态评分值
k=8640;                    %函数参数，隐藏可调
c=0.15958;
if x<60
    x=60;
    y=0.33*k*exp(-100*c)*(exp(c*(100-x))-1);
elseif x<70
    y=(-0.0067*x*x+0.938*x-31.83)*k*exp(-100*c)*(exp(c*(100-x))-1);
else
    y=k*exp(-100*c)*(exp(c*(100-x))-1);
end
end