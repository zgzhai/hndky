%%%%调用风险后果评估模型
function y=F_ZY(w1,w2)                  %w1,w2,w3,w4,w5,w6为不同风险后果的重要程度，默认值分别为0.1313，0.1863，0.0888，0.0888，0.2813，0.2234
    A=xlsread('BYQstate');              %变压器状态信息。
    V=A(3,1);                           %变压器容量
    b=A(4,1);                           %是否为常年保电主变标识
    I=A(5,1);                           %变压器高压侧电流
    y=w1*F1(V)+w2*F2(b,I);
end